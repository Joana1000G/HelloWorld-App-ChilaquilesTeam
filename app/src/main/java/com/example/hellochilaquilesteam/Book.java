package com.example.hellochilaquilesteam;

public class Book {

    private int id;
    private String name;
    private String author;
    private String year;

    public Book(int id, String author, String year) {
        this.id = id;
        this.name = name;
        this.author = author;
        this.year = year;
    }

    public int getId() {
        this.id = id;
    }



}
